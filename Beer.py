#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Παραγωγή Μπύρας - Brewing Process
Περιγραφή στα Αρχαία και Νέα Ελληνικά
"""

class ΖυθοποιίαProcess:
    """Κλάση για την περιγραφή της ζυθοποιίας (παραγωγής μπύρας)"""
    
    def __init__(self):
        self.βήματα_νέα = [
            "Προετοιμασία υλικών",
            "Άλεση κριθαριού", 
            "Εκχύλισμα (Mashing)",
            "Διύλιση",
            "Βρασμός με λυκίσκο",
            "Ψύξη",
            "Ζύμωση",
            "Ωρίμανση",
            "Εμφιάλωση"
        ]
        
        self.βήματα_αρχαία = [
            "παρασκευὴ ὑλῶν",
            "ἅλεσις κριθῆς",
            "ἐκχύλισμα ποίησις",
            "διήθησις",
            "ἕψησις μετὰ βρυωνίας",
            "κατάψυξις",
            "ζύμωσις",
            "ἡ πέπανσις",
            "εἰς ἀγγεῖα ἔνθεσις"
        ]
        
        self.υλικά_νέα = ["Κριθάρι", "Νερό", "Λυκίσκος", "Ζύμη"]
        self.υλικά_αρχαία = ["κριθή", "ὕδωρ", "βρυωνία", "ζύμη"]

    def περιγραφή_διαδικασίας(self):
        """Περιγράφει την πλήρη διαδικασία στα νέα ελληνικά"""
        print("🍺 ΠΑΡΑΓΩΓΗ ΜΠΥΡΑΣ - Σύγχρονη Περιγραφή 🍺")
        print("=" * 50)
        
        λεπτομέρειες = {
            "Προετοιμασία υλικών": "Συλλέγουμε κριθάρι, νερό, λυκίσκο και ζύμη",
            "Άλεση κριθαριού": "Αλέθουμε το κριθάρι για να γίνει αλεύρι",
            "Εκχύλισμα (Mashing)": "Αναμειγνύουμε το αλεύρι με ζεστό νερό",
            "Διύλιση": "Διυλίζουμε το μείγμα για να αφαιρέσουμε τα στερεά",
            "Βρασμός με λυκίσκο": "Βράζουμε το υγρό προσθέτοντας λυκίσκο για γεύση",
            "Ψύξη": "Ψύχουμε γρήγορα το μείγμα",
            "Ζύμωση": "Προσθέτουμε ζύμη για να αρχίσει η ζύμωση",
            "Ωρίμανση": "Αφήνουμε τη μπύρα να ωριμάσει για εβδομάδες",
            "Εμφιάλωση": "Βάζουμε τη μπύρα σε μπουκάλια ή βαρέλια"
        }
        
        for i, βήμα in enumerate(self.βήματα_νέα, 1):
            print(f"{i}. {βήμα}")
            print(f"   → {λεπτομέρειες[βήμα]}")
            print()

    def περιγραφὴ_ἀρχαία(self):
        """Περιγραφή στην αρχαία ελληνική - κλασική διάλεκτος"""
        print("🏛️ ΖΥΘΟΥ ΠΟΙΗΣΙΣ - Ἀρχαία Περιγραφή 🏛️")
        print("=" * 50)
        
        λεπτομέρειες_αρχαίες = {
            "παρασκευὴ ὑλῶν": "συλλέγομεν κριθὴν καὶ ὕδωρ καὶ βρυωνίαν καὶ ζύμην",
            "ἅλεσις κριθῆς": "ἀλέομεν τὴν κριθὴν ὥστε ἄλευρον γενέσθαι",
            "ἐκχύλισμα ποίησις": "μιγνύομεν τὸ ἄλευρον θερμῷ ὕδατι",
            "διήθησις": "διηθέομεν τὸ μῖγμα ἵνα τὰ στερεὰ ἀφέλωμεν",
            "ἕψησις μετὰ βρυωνίας": "ἕψομεν τὸ ὑγρὸν προστιθέντες βρυωνίαν εἰς χυμόν",
            "κατάψυξις": "ταχέως καταψύχομεν τὸ μῖγμα",
            "ζύμωσις": "προστίθεμεν ζύμην ἵνα ἡ ζύμωσις ἄρχηται",
            "ἡ πέπανσις": "ἐῶμεν τὸν ζῦθον πεπαίνεσθαι ἐπὶ ἑβδομάδας",
            "εἰς ἀγγεῖα ἔνθεσις": "εἰς ἀγγεῖα ἢ πίθους τὸν ζῦθον τίθεμεν"
        }
        
        for i, βήμα in enumerate(self.βήματα_αρχαία, 1):
            print(f"{i}. {βήμα}")
            print(f"   → {λεπτομέρειες_αρχαίες[βήμα]}")
            print()

    def σύγκριση_υλικών(self):
        """Σύγκριση υλικών στις δύο γλώσσες"""
        print("📋 ΥΛΙΚΑ - MATERIALS 📋")
        print("=" * 30)
        print("Νέα Ελληνικά    |  Ἀρχαῖα Ἑλληνικά")
        print("-" * 30)
        
        for νέο, αρχαίο in zip(self.υλικά_νέα, self.υλικά_αρχαία):
            print(f"{νέο:<15} |  {αρχαίο}")
        print()

    def ἱστορικὴ_σημείωσις(self):
        """Ιστορική σημείωση"""
        print("📜 ΙΣΤΟΡΙΚΗ ΣΗΜΕΙΩΣΗ 📜")
        print("=" * 40)
        print("Ἡ ζυθοποιία γνωστὴ ἦν τοῖς ἀρχαίοις Αἰγυπτίοις καὶ Μεσοποταμίοις.")
        print("Οἱ δὲ Ἕλληνες μᾶλλον οἶνον ἢ ζῦθον ἔπινον.")
        print("Ὁ Ἡρόδοτος λέγει ὅτι οἱ βάρβαροι ζῦθον ἐκ πυρῶν ἐποίουν.")
        print()
        print("Στη σύγχρονη εποχή, η ζυθοποιία έχει εξελιχθεί")
        print("σε επιστήμη με ακριβείς μετρήσεις και τεχνολογία.")

def main():
    """Κύρια συνάρτηση"""
    ζυθοποιία = ΖυθοποιίαProcess()
    
    print("🍺 ΤΕΧΝΗ ΖΥΘΟΠΟΙΙΑΣ 🍺")
    print("Η Παραγωγή Μπύρας στα Αρχαία και Νέα Ελληνικά")
    print("=" * 60)
    print()
    
    # Περιγραφή στα νέα ελληνικά
    ζυθοποιία.περιγραφή_διαδικασίας()
    print("\n" + "="*60 + "\n")
    
    # Περιγραφή στα αρχαία ελληνικά
    ζυθοποιία.περιγραφὴ_ἀρχαία()
    print("\n" + "="*60 + "\n")
    
    # Σύγκριση υλικών
    ζυθοποιία.σύγκριση_υλικών()
    
    # Ιστορική σημείωση
    ζυθοποιία.ἱστορικὴ_σημείωσις()
    
    print("\n🍻 Γεῖα σας καὶ καλὴν ζυθοποιίαν! 🍻")

if __name__ == "__main__":
    main()
